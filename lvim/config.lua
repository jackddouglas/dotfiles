          autocmd FileType markdown,mkd call pencil#init({"conceallevel": 2, "wrap": "soft"})
                                    \ | setl spell spl=en_gb
          autocmd FileType text         call pencil#init({"wrap": "hard"})
                                    \ | setl spell spl=en_gb
        augroup END
      ]])
		end,
	},
